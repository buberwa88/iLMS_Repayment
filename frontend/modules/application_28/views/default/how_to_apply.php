<p><strong>Instructions on how to use Integrated Loan Management System (iLMS)</strong></p>

<p><br>
<strong>General Instructions</strong></p>

<p><strong>Stage 1: Registration for an account:</strong></p>
 
<ul>
    <li><b>New applicants are applicants</b> that have not used this system(iLMS) before and don’t have accounts to enable them to login to the system using their username and password. These applicants are required to do Registration.</li>
	<li>Existing applicants are applicants who have completed Registration in this system(iLMS) before and were able to create accounts to enable them to login to the system using their username and password. These applicants can login to the system anytime.</li>
        <li><b>NECTA FORM 4 Index Number</b> should be in the format: <strong>S0XXX.XXXX or P0XXX.XXXX.</strong> 
            <strong>Example: S0750.0023 or P075.00023.</strong></li>
	<li>Applicant will be able to Login after completing Registration.</li>
        <li>Registration enables applicant to create account in this system(iLMS) and obtain Username and Password.</li>
        <li>Registration should only be done once. After completion, applicant should only Login using obtained username and password.</li>
        <li> Username will be applicant’s <strong>NECTA FORM 4 Index Number </strong> post fixed with a year of exam, like in this format: <strong>S0XXX.XXXX.YYYY or P0XXX.XXXX.YYYY</strong>
            <strong>Example: S0750.0023.2006 or P0750.0023.2006.</strong>
        <li> When logged in applicant will be able to <strong>make application fee payment</strong> and <strong>complete loan application</strong></li>
        <li><strong>Loan application forms </strong> will only be visible for filling once <strong>application fee is paid.</strong></li>
        <li> In order to successfully complete the loan application; applicant is required to <strong>complete all 13 steps under My Application link</strong>.</li>
        <li>Applicant will go through the following with the<strong> Loan application forms:</strong><ol>
                <li> Fill all application forms in the system </li>
                <li> Submit all the filled forms to the system </li>
                <li>Print the forms</li>
                <li>Sign all required forms</li>
                <li>Upload the signed forms</li>
                <li>Close the loan application</li>
</ol> </li>
	 
</ul>
 
<p><strong>Stage 1  Registration:</strong></p>
<p> Register for an account with iLMS by clicking on the Register link.</p>
<p><strong>A form will open up with two buttons:</strong>
<ol>
    <li><strong>NECTA button</strong><br/>
Click this button if you are an applicant who did form 4 exams in Tanzania.
You will be required to fill the form that will show below the button</li>

    <li><strong>NON-NECTA button</strong><br/>
Click this button if you are an applicant who did form 4 exams overseas.
You will be required to fill the form that will show below the button.</li>
</ol>
<br>
<p><strong>Stage 2  Login:</strong></p>
<p>After registration. You can login and be allowed to the system by <b>clicking on the Login link.</b>
 A form will open up with two input fields.</p>
<p>Enter applicant’s <strong> Username and Password </strong> which were created during applicant’s Registration.</p>
<p><b>Click Login button </b>to be checked and be allowed into the system.</p>
<br>

<p><strong>Stage 3  Important Instruction:</strong></p>
<p>When logged in to the system, you will be presented with important instructions that guards you about things to consider when applying for loan.</p>
 
 <p><strong>Stage 4 Application Fee Payment:</strong></p>
<p>Instructions on how to pay application fee and the status of application fee payment will be available under the <strong>Application Fee Payment link</strong>. </p>

 <p><strong>Stage 4 My Application:</strong></p>
 <p>Applicant can access his/her loan application forms through the My Application link.</p>
 <p>Application forms are divided into 9 sub-forms that are completed through first 8 different steps; just as shown once my application link opens a form.</p>
 
     